/* 
  Types:
  - primitive types (number, string, boolean, null, undefined, BigInt, Symbol).
  - reference types (everything else).
*/

/* 
  Mutability:

  - primitive types are immutable.
  - reference types are mutable.
*/

/* 
  - primitive types are passed by value. (usually stored in stack memory)
  - reference types are passed by reference. (usually stored in head memory)
*/

/* 
  Equality:

  - primitive types are compared by value.
  - reference types are compared by references. If two variables point to the same reference address in memory then it is the same object, otherwise,
  they can be compared by its content.
*/
